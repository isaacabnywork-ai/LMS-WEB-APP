function NavLink({ item, active, onNavigate }: { item: NavItem; active: boolean; onNavigate?: () => void }) {
  return (
    <Link
      href={item.href}
      onClick={onNavigate}
      className={`group flex items-center justify-between px-3 py-2.5 rounded-xl font-semibold text-sm transition-all duration-150 ${
        active
          ? "bg-[var(--sidebar-active-bg)] text-[var(--sidebar-active-text)] border-l-[3px] border-[var(--sidebar-active-border)] pl-[9px]"
          : "text-[var(--sidebar-text)] hover:bg-[var(--sidebar-hover-bg)] hover:text-slate-900 dark:hover:text-slate-100 border-l-[3px] border-transparent pl-[9px]"
      }`}
    >
      <div className="flex items-center space-x-3">
        <span className={`transition-colors duration-150 ${active ? "text-[var(--sidebar-active-text)]" : "text-slate-400 group-hover:text-slate-600 dark:group-hover:text-slate-300"}`}>
          {item.icon}
        </span>
        <span>{item.label}</span>
      </div>
      {item.badge && (
        <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${
          active
            ? "bg-teal-500 text-white"
            : "bg-teal-100 text-teal-700 dark:bg-teal-900/50 dark:text-teal-300"
        }`}>
          {item.badge}
        </span>
      )}
    </Link>
  );
}

function SidebarContent({ user, pathname, onNavigate }: {
  user: any;
  pathname: string;
  onNavigate?: () => void;
}) {
  const roleView = user.role === "admin" ? "admin" : user.role === "teacher" ? "teacher" : "student";
  
  let navItems: NavItem[] = [];
  let menuTitle = "";
  let modeBadge = "";

  if (roleView === "admin") {
    navItems = systemNav;
    menuTitle = "System Admin";
    modeBadge = "Admin Mode";
  } else if (roleView === "teacher") {
    navItems = teacherNav;
    menuTitle = "Teacher Menu";
    modeBadge = "Instructor Mode";
  } else {
    navItems = studentNav;
    menuTitle = "Student Menu";
    modeBadge = "Student Mode";
  }

  return (
    <>
      {/* Logo */}
      <div className="p-5 border-b border-[var(--sidebar-border)] flex items-center shrink-0">
        <img src="/logo.png" alt="Avia Tech Logo" className="h-10 w-auto block dark:hidden" />
        <img src="/logo-dark.png" alt="Avia Tech Logo" className="h-10 w-auto hidden dark:block" />
      </div>

      <div className="px-4 pt-4 pb-2 shrink-0">
        <div className="flex bg-slate-100 dark:bg-slate-800/60 rounded-xl p-3 gap-2 items-center justify-center border border-[var(--sidebar-border)] shadow-sm">
          <span className="text-xs font-bold uppercase tracking-widest text-slate-500 dark:text-slate-400">
            {modeBadge}
          </span>
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 overflow-y-auto py-2 px-3 thin-scrollbar">
        <p className="text-[10px] font-black text-[var(--sidebar-section-label)] uppercase tracking-widest px-3 py-2 mt-1">
          {menuTitle}
        </p>
        <div className="space-y-0.5">
          {navItems.map((item) => (
            <NavLink
              key={item.href}
              item={item}
              active={pathname === item.href || pathname.startsWith(item.href + "/")}
              onNavigate={onNavigate}
            />
          ))}
        </div>
      </nav>

      {/* User Profile & Logout */}
      <div className="p-4 border-t border-[var(--sidebar-border)] shrink-0 flex flex-col gap-2">
        <div className="flex items-center space-x-3 px-2 py-2 rounded-xl bg-[var(--sidebar-hover-bg)] transition-colors">
          <div className="w-9 h-9 rounded-full bg-gradient-to-br from-teal-400 to-teal-600 flex items-center justify-center text-white font-bold text-sm shrink-0 overflow-hidden">
            {user?.image ? (
              <img src={user.image} alt={user.name} className="w-full h-full object-cover" />
            ) : (
              (user?.name || "U").substring(0, 2).toUpperCase()
            )}
          </div>
          <div className="flex-1 min-w-0">
            <p className="font-semibold text-sm text-slate-900 dark:text-white truncate">{user?.name}</p>
            <p className="text-xs text-slate-500 capitalize">{roleView}</p>
          </div>
        </div>
        <button
          onClick={() => signOut({ callbackUrl: "/" })}
          className="flex items-center justify-center w-full space-x-2 px-3 py-2.5 rounded-xl text-rose-500 hover:bg-rose-500/10 hover:text-rose-600 font-semibold text-sm transition-all duration-150"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          <span>Log out</span>
        </button>
      </div>
    </>
  );
}

export function Sidebar({ user }: { user: any }) {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);

  // Close sidebar on route change (mobile)
  useEffect(() => {
    setMobileOpen(false);
  }, [pathname]);

  // Prevent body scroll when mobile drawer is open
  useEffect(() => {
    if (mobileOpen) {
      document.body.style.overflow = "hidden";
    } else {
      document.body.style.overflow = "";
    }
    return () => { document.body.style.overflow = ""; };
  }, [mobileOpen]);

  return (
    <>
      {/* ─── Desktop Sidebar ─── */}
      <aside className="hidden lg:flex w-64 h-screen bg-[var(--sidebar-bg)] border-r border-[var(--sidebar-border)] flex-col z-20 shrink-0 shadow-sm">
        <SidebarContent
          user={user}
          pathname={pathname}
        />
      </aside>

      {/* ─── Mobile: Hamburger Button (shown in header via portal) ─── */}
      <button
        onClick={() => setMobileOpen(true)}
        className="lg:hidden fixed top-3.5 left-4 z-30 p-2 rounded-xl bg-white dark:bg-slate-800 border border-slate-200 dark:border-slate-700 shadow-sm text-slate-600 dark:text-slate-300 hover:bg-slate-50 dark:hover:bg-slate-700 transition-colors"
        aria-label="Open menu"
      >
        <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M4 6h16M4 12h16M4 18h16" />
        </svg>
      </button>

      {/* ─── Mobile Overlay ─── */}
      {mobileOpen && (
        <div
          className="lg:hidden fixed inset-0 bg-black/40 backdrop-blur-sm z-40 transition-opacity"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* ─── Mobile Drawer ─── */}
      <aside
        className={`lg:hidden fixed top-0 left-0 h-full w-72 bg-[var(--sidebar-bg)] border-r border-[var(--sidebar-border)] flex flex-col z-50 shadow-2xl transition-transform duration-300 ease-in-out ${
          mobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {/* Close button */}
        <button
          onClick={() => setMobileOpen(false)}
          className="absolute top-4 right-4 p-1.5 rounded-lg text-slate-400 hover:text-slate-700 dark:hover:text-slate-200 hover:bg-slate-100 dark:hover:bg-slate-800 transition-colors"
          aria-label="Close menu"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>

        <SidebarContent
          user={user}
          pathname={pathname}
          onNavigate={() => setMobileOpen(false)}
        />
      </aside>
    </>
  );
}
